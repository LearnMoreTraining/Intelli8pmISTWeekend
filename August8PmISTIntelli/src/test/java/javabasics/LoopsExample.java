package javabasics;

public class LoopsExample {

    public static void main(String[] args) {

        int [] n = {56,77,88,100};

        for(int k =0 ; k < n.length;k++){

            System.out.println(n[k]);
        }

        String [] name = {"lmti","learnmore"}; // 0,1,2

        for(int j=0 ; j < name.length ; j++){

            System.out.println(name[j]);
        }


    }
}
